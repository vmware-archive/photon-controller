package com.vmware.photon.controller.common.xenon.deployment;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * This annotation indicates that a given ServiceDocument will not be migrated during deployment.
 *
 * This annotation should be used on Task and Trigger services or entities that should not be
 * migrated between different versions of the control plane.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.TYPE)
public @interface NoMigrationDuringDeployment {

}
